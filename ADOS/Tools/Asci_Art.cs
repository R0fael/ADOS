        public static string[] Symbol_Change(string[] symbol_big, char symbol = '@')
        {
            return new string[] { symbol_big[0].Replace('@', symbol), symbol_big[1].Replace('@', symbol), symbol_big[2].Replace('@', symbol), symbol_big[3].Replace('@', symbol), symbol_big[4].Replace('@', symbol), };
        }


        public static string[] Symbol(char sym, char symbol)
        {
            return sym switch
            {
                'A' => Symbol_Change(A, symbol),
                'B' => Symbol_Change(B, symbol),
                'C' => Symbol_Change(C, symbol),
                'D' => Symbol_Change(D, symbol),
                'E' => Symbol_Change(E, symbol),
                'F' => Symbol_Change(F, symbol),
                'G' => Symbol_Change(G, symbol),
                'H' => Symbol_Change(H, symbol),
                'I' => Symbol_Change(I, symbol),
                'J' => Symbol_Change(J, symbol),
                'K' => Symbol_Change(K, symbol),
                'L' => Symbol_Change(L, symbol),
                'M' => Symbol_Change(M, symbol),
                'N' => Symbol_Change(N, symbol),
                'O' => Symbol_Change(O, symbol),
                'P' => Symbol_Change(P, symbol),
                'Q' => Symbol_Change(Q, symbol),
                'R' => Symbol_Change(R, symbol),
                'S' => Symbol_Change(S, symbol),
                'T' => Symbol_Change(T, symbol),
                'U' => Symbol_Change(U, symbol),
                'V' => Symbol_Change(V, symbol),
                'W' => Symbol_Change(W, symbol),
                'X' => Symbol_Change(X, symbol),
                'Y' => Symbol_Change(Y, symbol),
                'Z' => Symbol_Change(Z, symbol),
                '-' => Symbol_Change(minus, symbol),
                '+' => Symbol_Change(plus, symbol),
                ' ' => Symbol_Change(space, symbol),
                _ => Symbol_Change(minus, symbol),
            };
        }

        public static string[] Text(string txt, char symbol = '@')
        {

            string[] reg = { "", "", "", "", "" };
            string[] load;
            foreach (char s in txt)
            {
                load = Symbol(s, symbol);
                reg[0] += load[0] + "  ";
                reg[1] += load[1] + "  ";
                reg[2] += load[2] + "  ";
                reg[3] += load[3] + "  ";
                reg[4] += load[4] + "  ";
            }
            return reg;
        }

